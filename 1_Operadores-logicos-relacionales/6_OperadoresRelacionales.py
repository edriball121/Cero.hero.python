# Operadores relacionales en Python

a = 10
b = 20

# Igual a (==)
print(a == b)  # False

# Diferente de (!=)
print(a != b)  # True

# Mayor que (>)
print(a > b)  # False

# Menor que (<)
print(a < b)  # True

# Mayor o igual que (>=)
print(a >= b)  # False

# Menor o igual que (<=)
print(a <= b)  # True