# Ejemplo de operadores de asignación en Python

# Operador de asignación simple
x = 10
print("x =", x)

# Operador de asignación con suma
x += 5  # Equivalente a x = x + 5
print("x += 5 ->", x)

# Operador de asignación con resta
x -= 3  # Equivalente a x = x - 3
print("x -= 3 ->", x)

# Operador de asignación con multiplicación
x *= 2  # Equivalente a x = x * 2
print("x *= 2 ->", x)

# Operador de asignación con división
x /= 4  # Equivalente a x = x / 4
print("x /= 4 ->", x)

# Operador de asignación con módulo
x %= 3  # Equivalente a x = x % 3
print("x %= 3 ->", x)

# Operador de asignación con división entera
x //= 2  # Equivalente a x = x // 2
print("x //= 2 ->", x)

# Operador de asignación con exponenciación
x **= 3  # Equivalente a x = x ** 3
print("x **= 3 ->", x)

# Operador de asignación con AND bit a bit
x &= 2  # Equivalente a x = x & 2
print("x &= 2 ->", x)

# Operador de asignación con OR bit a bit
x |= 1  # Equivalente a x = x | 1
print("x |= 1 ->", x)

# Operador de asignación con XOR bit a bit
x ^= 2  # Equivalente a x = x ^ 2
print("x ^= 2 ->", x)

# Operador de asignación con desplazamiento a la derecha
x >>= 1  # Equivalente a x = x >> 1
print("x >>= 1 ->", x)

# Operador de asignación con desplazamiento a la izquierda
x <<= 2  # Equivalente a x = x << 2
print("x <<= 2 ->", x)