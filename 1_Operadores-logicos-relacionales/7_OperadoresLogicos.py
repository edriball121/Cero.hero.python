a = True
b = False

# Operador AND
resultado_and = a and b
print(f"{a} AND {b} = {resultado_and}")

# Operador OR
resultado_or = a or b
print(f"{a} OR {b} = {resultado_or}")

# Operador NOT
resultado_not_a = not a
resultado_not_b = not b
print(f"NOT {a} = {resultado_not_a}")
print(f"NOT {b} = {resultado_not_b}")