# Ejemplos de operadores aritméticos en Python

# Suma
a = 10
b = 5
suma = a + b
print("Suma:", suma)  # Output: Suma: 15

# Resta
resta = a - b
print("Resta:", resta)  # Output: Resta: 5

# Multiplicación
multiplicacion = a * b
print("Multiplicación:", multiplicacion)  # Output: Multiplicación: 50

# División
division = a / b
print("División:", division)  # Output: División: 2.0

# División entera
division_entera = a // b
print("División Entera:", division_entera)  # Output: División Entera: 2

# Módulo
modulo = a % b
print("Módulo:", modulo)  # Output: Módulo: 0

# Exponenciación
exponenciacion = a ** b
print("Exponenciación:", exponenciacion)  # Output: Exponenciación: 100000